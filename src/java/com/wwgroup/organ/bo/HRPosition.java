package com.wwgroup.organ.bo;

public class HRPosition {

	private String positionName;

	private int positionId;

	private String cmpcod;

	private String depcod;

	private String posicod;

	private String a_depcode;

	public String getPositionName() {
		return positionName;
	}

	public void setPositionName(String positionName) {
		this.positionName = positionName;
	}

	public int getPositionId() {
		return positionId;
	}

	public void setPositionId(int positionId) {
		this.positionId = positionId;
	}

	public String getCmpcod() {
		return cmpcod;
	}

	public void setCmpcod(String cmpcod) {
		this.cmpcod = cmpcod;
	}

	public String getDepcod() {
		return depcod;
	}

	public void setDepcod(String depcod) {
		this.depcod = depcod;
	}

	public String getPosicod() {
		return posicod;
	}

	public void setPosicod(String posicod) {
		this.posicod = posicod;
	}

	public String getA_depcode() {
		return a_depcode;
	}

	public void setA_depcode(String a_depcode) {
		this.a_depcode = a_depcode;
	}

}
